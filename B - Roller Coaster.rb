# 問題文
# 高橋君の仲間たちは 
# N 人で遊園地に遊びにいきました。

# 遊園地の一番人気のジェットコースターに乗るためには、身長が 
# K cm以上必要です。

# 高橋君の 
# i 番目の仲間の身長は hicm です。

# 高橋君の仲間たちのうち、一番人気のジェットコースターに乗ることができる人の数を求めてください。

# 制約
# 1≤N≤105
# 1≤K≤500
# 1≤hi≤500
# 入力はすべて整数

# 回答
# ステップ1: 入力の取得
n, k = gets.split.map(&:to_i)
heights = gets.split.map(&:to_i)

# ステップ2: カウントの実行
count = heights.count { |h| h >= k }

# ステップ3: 結果の出力
puts count